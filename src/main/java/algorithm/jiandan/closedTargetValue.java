    public static int closestTargetValue3(int target, int[] array) {
        // Write your code here
        Arrays.sort(array);

        int start = 0;
        int end = array.length - 1;
        int min = Integer.MAX_VALUE;
        while (start < end) {
            int number = array[start] + array[end];
            if (number < target) {
                start++;
                min = Math.min((target - number), min);
            } else if (number > target) {
                end--;
            } else {
                return target;
            }
        }
        if (min == Integer.MAX_VALUE) {
            return -1;
        }

        return target - min;

    }

    public static int closestTargetValue2(int target, int[] array) {
        // Write your code here
        Arrays.sort(array);
        int res = array[0] + array[1];
        for (int i = 0; i < array.length; i++) {
            int v = array[i];
            for (int i1 = i + 1; i1 < array.length; i1++) {
                int v1 = array[i1];
                int n = v + v1;
                if (n <= target) {
                    if (target < res || n > res) {
                        res = n;
                    }
                }
            }
        }
        if (res > target) {
            return -1;
        }
        return res;
    }

