    public static int closestTargetValue(int target, int[] array) {
        // Write your code here
        Arrays.sort(array);

        int i = 0, j = array.length - 1;
        int closest = Integer.MIN_VALUE;

        while (i < j) {
            while (i < j && array[i] + array[j] > target) {
                j--;
            }

            while (i < j && array[i] + array[j] <= target) {
                closest = Math.max(closest, array[i] + array[j]);
                i++;
            }
        }

        return closest == Integer.MIN_VALUE ? - 1 : closest;
    }

